# ----------------------------------------------------------------------------
# Случай 1: Идентичные коэффициенты взаимодействия (симметричная конкуренция)
# Уравнения: dM₁/dθ = M₁ - A₁·M₁² - B·M₁·M₂
#            dM₂/dθ = C₂·M₂ - A₂·M₂² - B·M₁·M₂
# ----------------------------------------------------------------------------

println("\n" * "="^70)
